function buildNameList() {
  const seen = new Set();
  const out = [];
  for (const line of RAW.split('\n')) {
    const s = line.trim();
    if (!s || seen.has(s)) continue;
    seen.add(s);
    out.push(s);
  }
  return out;
}

export const CHINESE_TRADITIONAL_COLOR_NAMES = buildNameList();

/** FNV-1a 32-bit — stable index from YYYY-MM-DD */
export function pickChineseColorNameForDateKey(dateKey) {
  const names = CHINESE_TRADITIONAL_COLOR_NAMES;
  if (!names.length) return '中国色';
  let h = 2166136261;
  for (let i = 0; i < dateKey.length; i++) {
    h ^= dateKey.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  const idx = Math.abs(h) % names.length;
  return names[idx];
}
